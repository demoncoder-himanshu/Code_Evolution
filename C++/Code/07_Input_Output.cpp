#include <iostream>
#include <string>
using namespace std;

int main()
{
    // ===========================================================
    //                    INPUT & OUTPUT
    // ===========================================================


    // -----------------------------------------------------------
    // cout (Standard Output Stream)
    // -----------------------------------------------------------

    cout << "Welcome to Code Evolution." << endl;
    cout << "Normal Output." << endl;


    // -----------------------------------------------------------
    // cerr (Standard Error Stream)
    // -----------------------------------------------------------

    cerr << "Error: Invalid Input!" << endl;
    cerr << "Error: File Not Found!" << endl;


    // -----------------------------------------------------------
    // clog (Standard Log Stream)
    // -----------------------------------------------------------

    clog << "Log: Program Started." << endl;
    clog << "Log: Database Connected." << endl;
    clog << "Log: Program Finished." << endl;
    


    // -----------------------------------------------------------
    // Multiple Output
    // -----------------------------------------------------------

    int age = 19;

    cout << "Age : " << age;


    // -----------------------------------------------------------
    // Escape Sequences
    // -----------------------------------------------------------

    cout << "\n";   // New Line
    cout << "\t";   // Tab Space
    cout << "\"";   // Double Quotes
    cout << "\'";   // Single Quote
    cout << "\\";   // Backslash


    // -----------------------------------------------------------
    // INPUT (cin)
    // -----------------------------------------------------------

    int marks;

    cin >> marks;


    // -----------------------------------------------------------
    // Multiple Input
    // -----------------------------------------------------------

    int a, b;

    cin >> a >> b;


    // -----------------------------------------------------------
    // Character Input
    // -----------------------------------------------------------

    char grade;

    cin >> grade;


    // -----------------------------------------------------------
    // String Input (Single Word)
    // -----------------------------------------------------------

    string name;

    cin >> name;


    // -----------------------------------------------------------
    // String Input (Full Line)
    // -----------------------------------------------------------

    string fullName;

    getline(cin, fullName);


    // -----------------------------------------------------------
    // Ignore Input Buffer (Introduction)
    // -----------------------------------------------------------

    cin.ignore();


    return 0;
}